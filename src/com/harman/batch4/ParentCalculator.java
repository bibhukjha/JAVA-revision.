package com.harman.batch4;

public class ParentCalculator {

    public int Substraction (int x, int y)
    {
        return x-y;
    }

    public int Addition (int x, int y)
    {
        return x+y;
    }

    public int Multiply (int x, int y)
    {
        return x*y;
    }

    public int Divide (int x, int y)
    {
        return x/y;
    }
}
